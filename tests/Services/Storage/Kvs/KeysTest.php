    {
        if (UnsupportedMockTests::$skip) {
            $this->markTestSkipped('Mock server tests are disabled');
        }

        $result = $this->client->storage->kvs->keys->retrieve(
            'key',
            id: '182bd5e5-6e1a-4fe4-a799-aa6d9a6ab26e'
        );

